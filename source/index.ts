export function px (value: string | number) {
  return `${value}px`
}

export function pt (value: string | number) {
  return `${value}pt`
}

export function pct (value: string | number) {
  return `${value}%`
}

export function cm (value: string | number) {
  return `${value}cm`
}

export function mm (value: string | number) {
  return `${value}mm`
}

export function em (value: string | number) {
  return `${value}em`
}

export function rem (value: string | number) {
  return `${value}rem`
}
